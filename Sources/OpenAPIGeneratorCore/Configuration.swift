public struct ComponentPath: RawRepresentable, Codable, Hashable, Sendable {
    public var parent: String
    public var child: String?

    public init(parent: String, child: String?) {
        self.parent = parent
        self.child = child
    }

    public init?(rawValue: String) {
        let components = rawValue.components(separatedBy: ".")
        switch components.count {
        case 1:
            self.init(parent: components[0], child: nil)
        case 2:
            self.init(parent: components[0], child: components[1])
        default:
            return nil
        }
    }

    public var rawValue: String {
        if let child {
            "\(parent).\(child)"
        } else {
            parent
        }
    }
}

