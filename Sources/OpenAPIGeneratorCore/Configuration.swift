        enumConformances = try container.decodeIfPresent(Set<String>.self, forKey: .enumConformances) ?? enumConformances
        exclude = try container.decodeIfPresent(Set<ComponentPath>.self, forKey: .exclude) ?? exclude
        filenameTemplate = try container.decodeIfPresent(String.self, forKey: .filenameTemplate) ?? filenameTemplate
        include = try container.decodeIfPresent(Set<String>.self, forKey: .include) ?? include
        indirectProperties = try container.decodeIfPresent(Set<String>.self, forKey: .indirectProperties) ?? indirectProperties
        inlineReferencedSchemas = try container.decodeIfPresent(Bool.self, forKey: .inlineReferencedSchemas) ?? inlineReferencedSchemas
        imports = try container.decodeIfPresent(Set<String>.self, forKey: .imports) ?? imports
        memberwiseInit = try container.decodeIfPresent(Bool.self, forKey: .memberwiseInit) ?? memberwiseInit
        mutableProperties = try container.decodeIfPresent(Set<MutableProperties>.self, forKey: .mutableProperties) ?? mutableProperties
        nameTemplate = try container.decodeIfPresent(String.self, forKey: .nameTemplate) ?? nameTemplate
