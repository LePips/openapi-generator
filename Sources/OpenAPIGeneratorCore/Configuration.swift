import Foundation
import Yams

public struct Configuration: Decodable, Sendable {
    public enum Generate: String, Codable, Sendable {
        case entities
        case package
        case paths
    }

    public enum Access: String, Codable, Sendable {
        case `internal`
        case `public`
    }

    public var access: Access = .public
    public var acronyms: [String] = Self.defaultAcronyms
    public var comments: Comments = .init()
    public var dataTypes: DataTypes = .init()
    public var entities: Entities = .init()
    public var extensions: Extensions = .init()
    public var fileHeader: String = Self.defaultFileHeaderComment
    public var generate: Set<Generate> = [.entities, .paths]
    public var inlineTypealiases: Bool = true
    public var module: String = "MyOpenAPISDK"
    public var paths: Paths = .init()
    public var pluralizeProperties: Bool = true
    public var rename: Rename = .init()
    public var useSwiftyPropertyNames: Bool = true

    public init() {}

    enum CodingKeys: String, CodingKey {
        case access
        case acronyms
        case comments
        case dataTypes
        case entities
        case extensions
        case fileHeader
        case generate
        case inlineTypealiases
        case module
        case paths
        case pluralizeProperties
        case rename
        case useSwiftyPropertyNames
    }

    public init(from decoder: Decoder) throws {
        self.init()
        let container = try decoder.container(keyedBy: CodingKeys.self)
        access = try container.decodeIfPresent(Access.self, forKey: .access) ?? access
        acronyms = try container.decodeIfPresent([String].self, forKey: .acronyms) ?? acronyms
        comments = try container.decodeIfPresent(Comments.self, forKey: .comments) ?? comments
        dataTypes = try container.decodeIfPresent(DataTypes.self, forKey: .dataTypes) ?? dataTypes
        entities = try container.decodeIfPresent(Entities.self, forKey: .entities) ?? entities
        extensions = try container.decodeIfPresent(Extensions.self, forKey: .extensions) ?? extensions
        fileHeader = try container.decodeIfPresent(String.self, forKey: .fileHeader) ?? fileHeader
        generate = try container.decodeIfPresent(Set<Generate>.self, forKey: .generate) ?? generate
        inlineTypealiases = try container.decodeIfPresent(Bool.self, forKey: .inlineTypealiases) ?? inlineTypealiases
        module = try container.decodeIfPresent(String.self, forKey: .module) ?? module
        paths = try container.decodeIfPresent(Paths.self, forKey: .paths) ?? paths
        pluralizeProperties = try container.decodeIfPresent(Bool.self, forKey: .pluralizeProperties) ?? pluralizeProperties
        rename = try container.decodeIfPresent(Rename.self, forKey: .rename) ?? rename
        useSwiftyPropertyNames = try container.decodeIfPresent(Bool.self, forKey: .useSwiftyPropertyNames) ?? useSwiftyPropertyNames
    }
}

