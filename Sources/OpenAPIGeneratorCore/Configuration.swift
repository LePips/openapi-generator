public extension Entities {
    func preferredType(of entity: String) -> Entities.EntityType {
        entityTypeOverrides[entity] ?? defaultType
    }
}

public struct Paths: Decodable, Sendable {

    public var bodyTypeOverrides: [String: String] = [:]
    public var exclude: Set<String> = []
    public var filenameTemplate: String = "%0.swift"
    public var include: Set<String> = []
    public var imports: Set<String> = ["Foundation", "Get"]
    public var inlineQueryParameterLimit: Int? = 2
    public var inlineSimpleRequests: Bool = true
    public var namespace: String = "Paths"
    public var responseTypeOverrides: [String: String] = [:]

    public init() {}

    enum CodingKeys: String, CodingKey {
        case bodyTypeOverrides
        case exclude
        case filenameTemplate
        case include
        case imports
        case inlineQueryParameterLimit
        case inlineSimpleRequests
        case namespace
        case responseTypeOverrides
    }

    public init(from decoder: Decoder) throws {
        self.init()
        let container = try decoder.container(keyedBy: CodingKeys.self)
        bodyTypeOverrides = try container.decodeIfPresent([String: String].self, forKey: .bodyTypeOverrides) ?? bodyTypeOverrides
        exclude = try container.decodeIfPresent(Set<String>.self, forKey: .exclude) ?? exclude
        filenameTemplate = try container.decodeIfPresent(String.self, forKey: .filenameTemplate) ?? filenameTemplate
        include = try container.decodeIfPresent(Set<String>.self, forKey: .include) ?? include
        imports = try container.decodeIfPresent(Set<String>.self, forKey: .imports) ?? imports
        if container.contains(.inlineQueryParameterLimit) {
            inlineQueryParameterLimit = try container.decodeIfPresent(Int.self, forKey: .inlineQueryParameterLimit)
        }
        inlineSimpleRequests = try container.decodeIfPresent(Bool.self, forKey: .inlineSimpleRequests) ?? inlineSimpleRequests
        namespace = try container.decodeIfPresent(String.self, forKey: .namespace) ?? namespace
        responseTypeOverrides = try container
            .decodeIfPresent([String: String].self, forKey: .responseTypeOverrides) ?? responseTypeOverrides
    }
}

